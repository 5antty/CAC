ORG 3000H
COMP: PUSH AX
MOV CL, 4
MOV DL, 0
BUCLE: MOV BX, OFFSET CLAVE1
ADD BL, DL
MOV AL, [BX]
MOV BX, OFFSET CLAVE2
ADD BL, DL
MOV AH, [BX]
CMP AL, AH
JNZ FINR
INC DL
DEC CL
JNZ BUCLE
MOV DH, 1
FINR: POP AX
RET


ORG 2000H
