MOV CH, 4
LAZO: INT 6
INC BX
DEC CH
JNZ LAZO
CALL COMP
CMP DH, 1
JNZ NEGADO
MOV BX, OFFSET MSJ
MOV AL, OFFSET MSJ2 - OFFSET MSJ
INT 7
JMP FIN
NEGADO: MOV BX, OFFSET MSJ2
MOV AL, OFFSET CLAVE2 - OFFSET MSJ2
INT 7
FIN:INT 0
END
