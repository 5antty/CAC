ORG 1000H
SIGNO DB "-"
NUM1 DB ?
NUM2 DB ?
RES DB ?

ORG 3000H ; Subrutina MUL
RESTA: PUSH AX
MOV AL, NUM1
SUB AL, 30H
MOV AH, NUM2
SUB AH, 30H
SUB AL, AH
MOV RES, AL
POP AX
RET

MUL: PUSH AX
MOV CH, 0
CMP CL, 0
JZ FINM
CMP AL, 0
JZ FINM
LAZO: ADD CH, CL
DEC AL
JNZ LAZO
FINM: POP AX
RET


ORG 2000H
MOV BX, OFFSET NUM1
INT 6
MOV BX, OFFSET NUM2
INT 6
CALL RESTA
CMP RES, 0
JS NEGATIVO
ADD RES, 30H
MOV BX, OFFSET RES
MOV AL, 1
INT 7
JMP FIN
NEGATIVO: MOV BX, OFFSET SIGNO
MOV AL, 1
INT 7
MOV CL, RES
MOV AL, -2
CALL MUL
MOV RES, CH
MOV BX, OFFSET RES
MOV AL, 1
INT 7
FIN:INT 0
END