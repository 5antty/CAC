ORG 1000H
NUMS2DIG DB "101112131415161718"
NUM1 DB ?
NUM2 DB ?
RES DB ?

ORG 3000H ; Subrutina MUL
SUMA: PUSH AX
MOV AL, NUM1
SUB AL, 30H
MOV AH, NUM2
SUB AH, 30H
ADD AL, AH
MOV RES, AL
FINS: POP AX
RET

MUL: PUSH AX
MOV CH, 0
CMP CL, 0
JZ FINM
CMP AL, 0
JZ FINM
LAZO: ADD CH, CL
DEC AL
JNZ LAZO
FINM: POP AX
RET


ORG 2000H
MOV BX, OFFSET NUM1
INT 6
MOV BX, OFFSET NUM2
INT 6
CALL SUMA

CMP RES, 10
JNS DOSCIFRAS
ADD RES, 30H
MOV BX, OFFSET RES
MOV AL, 1
JMP FIN

DOSCIFRAS: MOV BX, OFFSET NUMS2DIG
MOV CL, RES
SUB CL, 10
MOV AL, 2
CALL MUL
MOV BL, CH
INT 7
FIN:INT 0

END